<p class="step-form__subtitle">
    <b>Правила подачи объявления:</b>
    В одном объявлении не допускается размещение нескольких позиций различных товаров или услуг. Например, если у вас
    есть несколько мотоциклов, квадроциклов и прицепов для аренды, необходимо создать отдельные объявления для каждой
    позиции.
</p>